#include<iostream>
using namespace std;
typedef unsigned int UINT;

////////////////////////////////////////////////////////////////////////////////////
//
// Function Nmae : ToggleBit
// Input : integer
// Output : returns iResult integer value
// Description : This function toggle ON first 4 bit of the number.
// Author Name : Gayatri Pravin Patil
// Date : 11/06/2023
//
////////////////////////////////////////////////////////////////////////////////////

UINT ToggleBit(UINT iNo)
{
    UINT iMask = 0X0000000F;
    UINT iResult = 0;

    iResult = iNo | iMask;

    return iResult;
}

////////////////////////////////////////////////////////////////////////////////////
//  Entry Point Function main
///////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iValue = 0;
    UINT iRet = 0;

    //////////////////////////////////////////////////
    // Accepting integer value fro user
    /////////////////////////////////////////////////

    cout<<"Enter number : "<<"\n";
    cin>>iValue;

    ////////////////////////////////////////////////////
    // Passing value to the bussiness logic function
    ///////////////////////////////////////////////////

    iRet = ToggleBit(iValue);
    cout<<"Result is: "<<iRet<<"\n";
   
    return 0;
}



    
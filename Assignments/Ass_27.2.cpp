#include<iostream>
using namespace std;
typedef unsigned int UINT;

////////////////////////////////////////////////////////////////////////////////////
//
// Function Nmae : OffBit
// Input : integer
// Output : returns iResult integer value
// Description : This function OFF 7th and 10th  bit of the number if it is ON.
// Author Name : Gayatri Pravin Patil
// Date : 11/06/2023
//
////////////////////////////////////////////////////////////////////////////////////

UINT OffBit(UINT iNo)
{
    UINT iMask = 0XFFFFFDBF;
    UINT iResult = 0;

    iResult = iNo & iMask;
    return iResult;
}

////////////////////////////////////////////////////////////////////////////////////
//  Entry Point Function main
///////////////////////////////////////////////////////////////////////////////////

int main()
{
    UINT iValue = 0;
    UINT iRet = 0;

    //////////////////////////////////////////////////
    // Accepting integer value fro user
    /////////////////////////////////////////////////

    cout<<"Enter number : "<<"\n";
    cin>>iValue;

    ////////////////////////////////////////////////////
    // Passing value to the bussiness logic function
    ///////////////////////////////////////////////////

    iRet = OffBit(iValue);
    cout<<"Result is: "<<iRet<<"\n";
   
    return 0;
}



    